/**
 * An enumeration that defines all the possible color rendering.
 * <ul>
 * <li>A device that is set to UNIVERSAL only responds to UNIVERSAL triggers.</li>
 * <li>A trigger that is set to UNIVERSAL can activate and deactivate ALL devices</li>
 * </ul>
 * 
 * @author Naufal Fikri
 *
 */
public enum TriggerColor {
	BLUE,GREEN,AQUA,RED,PURPLE,GOLD,UNIVERSAL
}
